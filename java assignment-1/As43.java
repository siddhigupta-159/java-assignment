class As43{
    public static void main(String args[]){
        double h=4;
        double l=5;
        double r=(l*l)-(h*h);
        double Rsquare=Math.sqrt(r); 
        double Pie=3.14;
        double cost=10;
        double A=Pie*Rsquare*Rsquare;
        double rate=cost*A;
        System.out.println("Radious:"+Rsquare);
        System.out.println("AREA:"+A);
        System.out.println("RATE:"+rate);

    }
}